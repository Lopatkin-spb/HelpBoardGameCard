    private int action;
    private int drawableResource;
    private int stringResource;

    SymbolIcon(int action, int drawableResource, int stringResource) {
        this.action = action;
        this.drawableResource = drawableResource;
        this.stringResource = stringResource;
    }

    public static int getDrawableResourceFrom(int action) {
        for (SymbolIcon value : values()) {
            if (value.action == action) {
                return value.drawableResource;
            }
        }
        throw new IllegalArgumentException("Unknown symbol icon action: " + action + " (" + SymbolIcon.class.getSimpleName() + ")");
    }

    public static int getDrawableResourceFrom(String codename) {
        String codenameWithoutSeparator = codename.substring(SEPARATOR.length());

        for (SymbolIcon value : values()) {
            if (value.name().equals(codenameWithoutSeparator)) {
                return value.drawableResource;
            }
        }
        return defaultIcon1.drawableResource;
    }

    public static String getCodenameFrom(int action) {
        for (SymbolIcon value : values()) {
            if (value.action == action) {
                return SEPARATOR + value.name();
            }
        }
        throw new IllegalArgumentException("Unknown symbol icon action: " + action + " (" + SymbolIcon.class.getSimpleName() + ")");
    }

    public static int getStringResourceFrom(String codename) {
        String codenameWithoutSeparator = codename.substring(SEPARATOR.length());

        for (SymbolIcon value : values()) {
            if (value.name().equals(codenameWithoutSeparator)) {
                return value.stringResource;
            }
        }
        throw new IllegalArgumentException("Unknown symbol icon codename: " + codename + " (" + SymbolIcon.class.getSimpleName() + ")");
    }

